                       <p>
                       O termo ética (do grego, ethos, significa costumes) é usado de duas formas, tanto como um conjunto de regras e costumes coletivos, quanto como o estudo da moral. Ela teve início como conceito e estudo na Grécia antiga com Sócrates, Aristóteles e Platão, na visão deles, esse conceito é diretamente envolto com o espírito, honra e virtude da pessoa, sendo o único caminho para uma vida pacífica. Apesar disso, outros pensadores como Confúcio já comentavam sobre costumes que devem ser padrões universais antes dos gregos.
	                   Com o passar do tempo, já na idade média, se chega na conclusão de que a ética tem um papel fundamental na religião. Santo Agostinho e São Tomás de Aquino reformularam a ideia de ética para que ela seja diretamente relacionada aos costumes cristãos. Atualmente, a ética não é mais diretamente relacionada com religião, sendo presente em qualquer coletivo, como empresas, grupos e trabalhos.
                       A ética é formada a partir do desenvolvimento e dos valores da sociedade, podemos dizer que é um conceito puramente teórico por se tratar de uma análise dela. A ética, além disso, também é universal e permanente, ou seja, enquanto a sociedade existir, continuará sendo errado roubar, matar inocentes ou até furar fila.
